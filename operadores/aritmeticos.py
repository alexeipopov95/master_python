#operadores aritmeticos

numero_uno = 75
numero_dos = 25

suma = numero_uno + numero_dos
resta = numero_uno - numero_dos
multiplicacion = numero_uno * numero_dos
division = numero_uno / numero_dos
division_exacta = numero_uno // numero_dos #nos devuelve la parte entera
modulo = numero_uno % numero_dos #el resto de la division
potencia = numero_uno ** 2

print(f'La suma es: {suma}')
print(f'La resta es: {resta}')
print(f'La multiplicacion es: {multiplicacion}')
print(f'La division es: {division}')
print(f'La division_exacta es: {division_exacta}')
print(f'La modulo es: {modulo}')
print(f'La potencia es: {potencia}')