"""
Ejercicio 4.
    -Pedir numeros y mostrar la suma, resta, multiplicacion y division(sin validar el 0 en este caso)
"""

numero_uno = int(input("Ingrese el primero numero: "))
numero_dos = int(input("Ingrese el segundo numero: "))

print(f'La suma es: {numero_uno + numero_dos}')
print(f'La resta es: {numero_uno - numero_dos}')
print(f'La multiplicacion es: {numero_uno * numero_dos}')
print(f'La division es: {numero_uno / numero_dos}')